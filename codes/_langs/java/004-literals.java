class Main{
	public static void main(String[] args){
		// What the hell are literals?
		// Literals are constant values assigned to a variable.
		// 	8.44 --> Double by default
		// 	8.44 ==> 8.44d
		// 	to make it float you have to do 8.44f
		byte age=34; // 34 is a literal here
		char ch='1'; // '1' is a char literal
		float f1=3.55f;
		float f2=3.55F;
		double d1=3.55d;
		double d2=3.55234;
		int number=88;
		long long_number=8888888888888888l; // have to specify "l" or "L" for long
		boolean a=true;
		String fuck="Fuck you";
		System.out.println(fuck);
	}
}
